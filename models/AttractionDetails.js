class AttractionDetails {
    constructor (
        name,
        openingHours,
        webUrl,
        desc,
        reviewRating,
        review
    ) {
        this.name = name;
        this.openingHours = openingHours;
        this.webUrl = webUrl;
        this.desc = desc;
        this.reviewRating = reviewRating;
        this.review = review;
    }
};

export default AttractionDetails;